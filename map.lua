-- Generate a quad or an image of each tile in the sprite sheet image
-- Need to start the loops at 0 in order to start at the beginning of the image
-- and then using the iteration counters to move alongside the image
function map.generate_quads(self)
    for row = 0, self.image_height / self.tile_height - 1 do 
        for column = 0 , self.image_width / self.tile_width -1 do
            table.insert(self.tiles, love.graphics.newQuad(column * self.tile_width, row * self.tile_width, self.tile_width, self.tile_height, self.image_width, self.image_height))
        end
    end
end

--Draw the tile map by placing a corresponding tile where a number is not 0
function map.draw_map(self)
    for i = 1, #self.tile_map do
        for j = 1, #self.tile_map[i] do
            if self.tile_map[i][j] ~= 0 then
                    love.graphics.draw(map.image, map.tiles[(self.tile_map[i][j])], (j - 1)* map.tile_width, (i - 1) * map.tile_height)
            end
        end
    end
end
